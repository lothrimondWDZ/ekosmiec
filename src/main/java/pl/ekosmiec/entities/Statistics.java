package pl.ekosmiec.entities;

public class Statistics {
	
	private String okres;
	private String ilosc_odpadow;
	private String czas_pracy;
	
	public String getOkres() {
		return okres;
	}
	public void setOkres(String okres) {
		this.okres = okres;
	}
	public String getIlosc_odpadow() {
		return ilosc_odpadow;
	}
	public void setIlosc_odpadow(String ilosc_odpadow) {
		this.ilosc_odpadow = ilosc_odpadow;
	}
	public String getCzas_pracy() {
		return czas_pracy;
	}
	public void setCzas_pracy(String czas_pracy) {
		this.czas_pracy = czas_pracy;
	}
	
	
}
